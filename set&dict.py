'''
字典是另一种可变容器模型，且可存储任意类型对象。
字典的每个键值(key=>value)对用冒号(:)分割，
每个对之间用逗号(,)分割，整个字典包括在花括号({})中

集合（set）是一个无序的不重复元素序列。
可以使用大括号 { } 或者 set() 函数创建集合，
注意：创建一个空集合必须用 set() 而不是 { }，因为 { } 是用来创建一个空字典
'''

d = {'a':1,'b':2,'c':3}
print(d)
print(d['b'])

a = set('adslkfjsdkjfsdfsadfsdflkjkjdfilkjd')
print(a)

